/*

Questão 03 – Crie um algoritmo usando o método forEach() para encontrar o 
maior número em um array de números?
*/

let array = [3,30, 2, 5];
let maironumerodecapa = 0;

array.forEach(ffmilgrau => {
  if (ffmilgrau > maironumerodecapa) {
    maironumerodecapa = ffmilgrau;
  }
});

console.log(maironumerodecapa);